select* from customers;
select count(*) from customers;

-- --Q1. What is the total revenue generated by male vs. female customers?
SELECT 
    gender, SUM(purchase_amount_usd) AS total_revenue
FROM
    customers
GROUP BY gender;


-- --Q2. Which customers used a discount but still spent more than the average purchase amount? 
SELECT 
    customer_id, purchase_amount_usd
FROM
    customers
WHERE
    discount_applied = 'yes'
        AND purchase_amount_usd > (SELECT 
            AVG(purchase_amount_usd)
        FROM
            customers);


-- Q3. Which are the top 5 products with the highest average review rating?
SELECT 
    item_purchased,
    AVG(review_rating) AS highest_avg_reveiw_rating
FROM
    customers
GROUP BY item_purchased
ORDER BY highest_avg_reveiw_rating DESC
LIMIT 5;

-- --Q4. Compare the average Purchase Amounts between Standard and Express Shipping. 
SELECT 
    shipping_type, AVG(purchase_amount_usd)
FROM
    customers
WHERE
    shipping_type IN ('standard' , 'express')
GROUP BY shipping_type;

-- --Q5. Do subscribed customers spend more? Compare average spend and total revenue between subscribers and non-subscribers.
SELECT 
    subscription_status,
    COUNT(customer_id) AS total_customers,
    SUM(purchase_amount_usd) AS total_purchase,
    ROUND(AVG(purchase_amount_usd), 2) AS avg_purchase
FROM
    customers
GROUP BY subscription_status
ORDER BY total_purchase DESC;


-- --Q6. Which 5 products have the highest percentage of purchases with discounts applied?
SELECT item_purchased,
       ROUND(100.0 * SUM(CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END)/COUNT(*),2) AS discount_rate
FROM customers
GROUP BY item_purchased
ORDER BY discount_rate DESC
LIMIT 5;


-- --Q7. Segment customers into New, Returning, and Loyal based on their total 
-- number of previous purchases, and show the count of each segment. 
with customer_type as (
SELECT customer_id, previous_purchases,
CASE 
    WHEN previous_purchases = 1 THEN 'New'
    WHEN previous_purchases BETWEEN 2 AND 10 THEN 'Returning'
    ELSE 'Loyal'
    END AS customer_segment
FROM customers)

select customer_segment,count(*) AS "Number of Customers" 
from customer_type 
group by customer_segment;


-- --Q8. What are the top 3 most purchased products within each category? 
WITH item_counts AS (
    SELECT category,
           item_purchased,
           COUNT(customer_id) AS total_orders,
           ROW_NUMBER() OVER (PARTITION BY category ORDER BY COUNT(customer_id) DESC) AS item_rank
    FROM customers
    GROUP BY category, item_purchased
)
SELECT item_rank,category, item_purchased, total_orders
FROM item_counts
WHERE item_rank <=3;

-- --Q9. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe?
SELECT subscription_status,
       COUNT(customer_id) AS repeat_buyers
FROM customers
WHERE previous_purchases > 5
GROUP BY subscription_status;


--  Q10 What is the revenue contribution of each age group? 
SELECT 
    age,
    SUM(purchase_amount_usd) AS total_revenue
FROM customers
GROUP BY age
ORDER BY total_revenue desc;




